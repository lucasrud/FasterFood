package de.fasterfood.fasterfood.editMeal;

public class MealDTO {
    private String name;
    private int price;

    public MealDTO() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
